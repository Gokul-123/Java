class arithmetic {
  public static void main(String[] args) {
    int s=10,r=5;
    System.out.println("The addition is "+ (s+r));
    System.out.println("The subraction is "+ (s-r));
    System.out.println("The multiplication is "+ (s*r));
    System.out.println("The division is "+ (s/r));
    System.out.println("The remainder is "+ (s%r));
  }
}
